import 'package:flutter/material.dart';

class CustomClipPath extends CustomClipper<Path> {
  final Size size;

  CustomClipPath({required this.size});

  @override
  Path getClip(Size size) {
    var path = Path();
    path.moveTo(size.width * 0.0025000, 0);
    path.lineTo(size.width, size.height * 0.0050000);
    path.lineTo(size.width * 1.0025000, size.height * 0.4983333);
    path.quadraticBezierTo(size.width * 0.7643750, size.height * 0.5027667, size.width * 0.7489000, size.height * 0.5027667);
    path.cubicTo(size.width * 0.7324833, size.height * 0.5021833, size.width * 0.7037583, size.height * 0.5165333, size.width * 0.7108917, size.height * 0.4649667);
    path.quadraticBezierTo(size.width * 0.6922917, size.height * 0.3981167, size.width * 0.4835417, size.height * 0.4059333);
    path.quadraticBezierTo(size.width * 0.3030000, size.height * 0.4216500, size.width * 0.2945417, size.height * 0.4660500);
    path.cubicTo(size.width * 0.2969667, size.height * 0.5087500, size.width * 0.2674583, size.height * 0.4988000, size.width * 0.2510250, size.height * 0.5038500);
    path.quadraticBezierTo(size.width * 0.2257250, size.height * 0.5024500, size.width * -0.0058333, size.height * 0.5027500);
    return path;
  }

  @override
  bool shouldReclip(CustomClipper<Path> oldClipper) {
    return true;
  }
}


